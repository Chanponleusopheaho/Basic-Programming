import math

print(math.pi, math.e)
print(math.radians(60))
print(math.degrees(math.pi), 'degrees')
print(math.sin(30), math.cos(90), math.tan(80))
print(math.log(10), math.log10(10))
print(math.exp(10))   # e^10
print(math.pow(2, 4))   # 2^4
print(math.sqrt(100))
print(math.ceil(4.67), math.floor(4.67))